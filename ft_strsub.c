#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*s_new;

	if ((int)len < 0 || !s)
		return (NULL);
	s_new = ft_strnew(len);
	if (!s_new)
		return (NULL);
	ft_strncpy(s_new, s + start, len);
	return (s_new);
}
